async def main():
    async with AsyncSessionLocal() as db:
        done = []
        for name, era in ERA.items():
            r = await db.execute(select(Poet).where(Poet.name == name))
            p = r.scalar_one_or_none()
            if p:
                p.era = era; done.append((name, era))
        await db.commit()
    print(f"updated {len(done)} poet eras")
    for n, e in done: print(f"   {e:28s} {n}")
    missing = [n for n in ERA if n not in [d[0] for d in done]]
    if missing: print("NOT FOUND (name mismatch):", missing)

asyncio.run(main())
